# Create list
students = ['Danny', 'Ange', 'Gloria', 'Straton', 'Latifah']

# Add new to list
students.append('Prince')
print(students)

# Remove student by name
students.remove('Danny')
print(students)

# Display students alphabetically
students.sort()
print(students)

# Convert list to set
students_set = set(students)
print(students_set)